//! `CURVES_WITH_ENDOMORPHISMS[1..7]` — the 6 NICE alternate starting curves
//! with their 2^F torsion bases + endomorphism-action matrices, Rust
//! transcriptions of the SQIsign C `endomorphism_action.c`. Curve data is
//! fp2 in CANONICAL form (the C stores Montgomery; extracted via the
//! fp_encode oracle → canonical bytes → `Fp2::from_bytes_le`). Action matrices
//! are `ibz` (GMP-64 extractor → `Int<8>`). Consumed by the dim2id2iso spine's
//! n_order≠0 path: start curve + basis_even + endomorphism_application.
//!
//! Index 0 here = C `CURVES_WITH_ENDOMORPHISMS[1]` (the offset-by-1 NICE table;
//! C index 0 is the standard curve E0). Matrices stored row-major
//! `[[m00, m01], [m10, m11]]`; field order i, j, k, gen2, gen3, gen4.

use crypto_bigint::{Int, Uint};

use crate::gf::fp::BaseField;
use crate::gf::fp2::Fp2;
use crate::params::lvl1::Fp1Element;

/// A NICE starting curve with its endomorphism data (the C
/// `curve_with_endomorphism_ring_t`): Montgomery `(curve_a : curve_c)`, the 2^F
/// torsion basis (`p`/`q`/`pmq` as `x:z`), and the six endomorphism-action
/// matrices (`i,j,k,gen2,gen3,gen4`, row-major mod 2^F).
#[allow(dead_code, missing_docs)]
#[derive(Clone, Debug)]
pub struct CurveWithEndomorphism {
    /// Montgomery A coefficient (Fp2, canonical).
    pub curve_a: Fp2<Fp1Element>,
    /// Montgomery C coefficient.
    pub curve_c: Fp2<Fp1Element>,
    /// 2^F basis: P, Q, P-Q, each x:z projective (Fp2).
    pub p_x: Fp2<Fp1Element>,
    pub p_z: Fp2<Fp1Element>,
    pub q_x: Fp2<Fp1Element>,
    pub q_z: Fp2<Fp1Element>,
    pub pmq_x: Fp2<Fp1Element>,
    pub pmq_z: Fp2<Fp1Element>,
    /// Endomorphism action matrices on the 2^F basis (mod 2^F), row-major.
    pub action_i: [[Int<8>; 2]; 2],
    pub action_j: [[Int<8>; 2]; 2],
    pub action_k: [[Int<8>; 2]; 2],
    pub action_gen2: [[Int<8>; 2]; 2],
    pub action_gen3: [[Int<8>; 2]; 2],
    pub action_gen4: [[Int<8>; 2]; 2],
}

#[inline]
fn le32(h: &str) -> [u8; 32] {
    let mut b = [0u8; 32];
    let mut k = 0;
    while k < 32 {
        b[k] = u8::from_str_radix(&h[2 * (31 - k)..2 * (31 - k) + 2], 16).expect("hex");
        k += 1;
    }
    b
}
#[inline]
fn fp2_be(re: &str, im: &str) -> Fp2<Fp1Element> {
    Fp2::new(
        Fp1Element::from_bytes_le(&le32(re))
            .into_option()
            .expect("canonical re"),
        Fp1Element::from_bytes_le(&le32(im))
            .into_option()
            .expect("canonical im"),
    )
}
// Action-matrix entries are reduced mod 2^F, hence always in [0, 2^F): no
// signed helper is needed.
#[inline]
fn iw(words: [u64; 8]) -> Int<8> {
    *Uint::<8>::from_words(words).as_int()
}

