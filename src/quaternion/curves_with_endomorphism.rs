#[cfg(test)]
mod tests {
    use super::*;

    /// Extraction validation: curve E0 = A:0, C:1; all 6 NICE curves have C=1
    /// (normalized) and a non-zero 2^F basis.
    #[test]
    fn curves_with_endomorphism_l1_extract_sane() {
        let e0 = curve_with_endomorphism_e0_l1();
        let zero = Fp2::<Fp1Element>::zero();
        let one = Fp2::<Fp1Element>::new(Fp1Element::one(), Fp1Element::zero());
        assert_eq!(e0.curve_a, zero, "E0 A must be 0");
        assert_eq!(e0.curve_c, one, "E0 C must be 1");
        assert_ne!(e0.p_x, zero, "E0 basis P.x must be non-zero");
        let curves = [
            curve_with_endomorphism_0_l1(),
            curve_with_endomorphism_1_l1(),
            curve_with_endomorphism_2_l1(),
            curve_with_endomorphism_3_l1(),
            curve_with_endomorphism_4_l1(),
            curve_with_endomorphism_5_l1(),
        ];
        for (k, c) in curves.iter().enumerate() {
            assert_eq!(c.curve_c, one, "NICE curve {k} C must be 1 (normalized)");
            assert_ne!(c.p_x, zero, "NICE curve {k} basis P.x must be non-zero");
            assert_ne!(c.q_x, zero, "NICE curve {k} basis Q.x must be non-zero");
        }
    }
}
